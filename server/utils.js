const intersection = require("lodash/intersection");

const filters = (ingredients, tools, tags, Ingredient, Tooles, Tags, type) => {
  const res = [];

  if ( type?.non_alcoholic || Tags.includes("безалкогольные")) {
    return type?.non_alcoholic && Tags.includes("безалкогольные");
  } else {
    if (tags) {
      const allItems = Tags.map((e) => e.Name);

      res.push(
        allItems.filter((e) => tags.find((g) => g === e)).length === Tags.length
      );
    }

    if (ingredients) {
      const allItems = Ingredient.filter((e) => e.require === undefined).map(
        (e) => e.Name
      );

      res.push(intersection(allItems, ingredients).length === allItems.length);
    }

    if (tools) {
      const allItems = Tooles.map((e) => e.Name);
      res.push(
        allItems.filter((e) => tools.find((g) => g === e)).length ===
          Tooles.length
      );
    }
    return res.every((e) => e);
  }
};

